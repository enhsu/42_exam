#include <unistd.h>

int	has_a(char *s)
{
	int	i;

	i = 0;
	while (s[i])
	{
		if (s[i] == 'a')
			return (1);
		i++;
	}
	return (0);
}

int	main(int ac, char **av)
{
	if (ac == 2)
	{
		if (has_a(av[1]))
			write(1, "a\n", 2);
		else
			write(1, "\n", 1);
	}
	else
		write(1, "a\n", 2);
	return (0);
}
