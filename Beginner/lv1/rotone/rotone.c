#include <unistd.h>

void	rotone(char *s)
{
	int		i;
	char	c;

	i = 0;
	while (s[i])
	{
		if (s[i] == 'z' || s[i] == 'Z')
			c = s[i] - ('z' - 'a');
		else if ((s[i] >= 'a' && s[i] < 'z') || (s[i] >= 'A' && s[i] < 'Z'))
			c = s[i] + 1;
		else
			c = s[i];
		write(1, &c, 1);
		i++;
	}
}

int		main(int ac, char **av)
{
	if (ac == 2)
		rotone(av[1]);
	write(1, "\n", 1);
	return (0);
}
