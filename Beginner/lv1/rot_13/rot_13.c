#include <unistd.h>

void	rot(char c)
{
	if ((c >= 'a' && c <= 'm') || (c >= 'A' && c <= 'M'))
	{
		c += 13;
		write(1, &c, 1);
	}
	else if ((c >= 'n' && c <= 'z') || (c >= 'N' && c <= 'Z'))
	{
		c -= 13;
		write(1, &c, 1);
	}
	else
		write(1, &c, 1);
}

int		main(int argc, char **argv)
{
	int	i;

	if (argc != 2)
	{
		write(1, "\n", 1);
		return (0);
	}
	i = 0;
	while (argv[1][i] != '\0')
	{
		rot(argv[1][i]);
		i++;
	}
	write(1, "\n", 1);
	return (0);
}
